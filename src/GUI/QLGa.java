    public String getmaGa(){
        String maGa = "";
        OracleConnection OC = new OracleConnection();
        Connection conn = null;
        try{
            conn = OC.getOracleConnection();
            conn.setTransactionIsolation(Connection.TRANSACTION_SERIALIZABLE);
            conn.setTransactionIsolation(Connection.TRANSACTION_READ_COMMITTED);
            String SQL1 = "SELECT MAGA FROM GA ORDER BY MAGA DESC";
            Statement pres1 = conn.createStatement();
            ResultSet rs1 = pres1.executeQuery(SQL1);
            if(rs1.next()==false){
                maGa = "GA01";
            } int MThientai = Integer.parseInt(rs1.getString(1).substring(3,rs1.getString(1).length()-1)) + 1;
                
                if (MThientai < 10){
                    maGa = "GA0" + MThientai;
                } else 
                    maGa = "GA" + MThientai; 
                      
        }
        catch (Exception e){
            e.printStackTrace();
        }
        return maGa;
    }
        public void LoadGa(){
        listGa.setRowCount(0);
        OracleConnection OC = new OracleConnection();
        Connection conn = null;
        try{
            conn = OC.getOracleConnection();
            conn.setTransactionIsolation(Connection.TRANSACTION_SERIALIZABLE);
            conn.setTransactionIsolation(Connection.TRANSACTION_READ_COMMITTED);
            String SQL = "SELECT * FROM GA ORDER BY MAGA ASC";
            
            Statement pres = conn.createStatement();
            
            ResultSet rs = pres.executeQuery(SQL);
            
            while(rs.next()){
                String r1 = rs.getString("MAGA");
                String r2 = rs.getString("TENGA");
                Object row[] = new Object[] {r1,r2};
                listGa.addRow(row);
            }
            conn.close();
            pres.close();
            rs.close();
            
        }catch (Exception e){
            e.printStackTrace();
        }
    }

