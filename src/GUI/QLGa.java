    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        listGa = (DefaultTableModel) tbga.getModel();
        listGa.setRowCount(0);
        String tracuu = '%'+txtTraCuu.getText()+'%';
        if(tracuu.equals("")){
            JOptionPane.showMessageDialog(null, "Không có thông tin!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        OracleConnection OC = new OracleConnection();
        Connection conn = null;
        try{
            conn = OC.getOracleConnection();
            String SQL = "SELECT * FROM GA WHERE MAGA LIKE ? OR TENGA LIKE ?";
            PreparedStatement pres = conn.prepareStatement(SQL);
            pres.setString(1, tracuu);
            pres.setString(2, tracuu);
            ResultSet rs = pres.executeQuery();
            while(rs.next()){
                String maga= rs.getString("MAGA");
                String tenga = rs.getString("TENGA");
                listGa.addRow(new Object[] {maga,tenga});
            }
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

