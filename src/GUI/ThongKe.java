                stt++;
                dtmTuyen.addRow(new Object[] {stt,rs.getString("TO_CHAR(TGIANKH,'YYYY-MM-DD')"),getTenGa(rs.getString("GAKH")),getTenGa(rs.getString("GAKT")),rs.getInt("SLVE"),rs.getInt("TONGTIEN")});
