            ChartPanel chartPanel = new ChartPanel(chart);
            chartPanel.setPreferredSize(new Dimension(pn_thongke_dt_tuyen.getWidth(), pn_thongke_dt_tuyen.getHeight() - 20));
            pn_thongke_dt_tuyen.removeAll();
