    private void loc_VeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loc_VeActionPerformed
        // TODO add your handling code here:
        Date ngbd, ngkt;
        ngbd = new java.sql.Date(jDateChooser1.getDate().getTime());
        ngkt = new java.sql.Date(jDateChooser2.getDate().getTime());
        if(ngbd.compareTo(ngkt) > 0){
            JOptionPane.showMessageDialog(null, "Ngày bắt đầu phải nhỏ hơn ngày kết thúc!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(ngkt.compareTo(datenow) >= 0){
            JOptionPane.showMessageDialog(null, "Ngày kết thúc phải nhỏ hơn ngày hôm nay!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String ngaybd, ngaykt;
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dtmVe = (DefaultTableModel) tbVe.getModel();
        dtmVe.setRowCount(0);
        OracleConnection OC = new OracleConnection();
        try{
            Connection conn = OC.getOracleConnection();
            String SQL = "SELECT NGAYDATVE, COUNT(MAVE) AS SLVE, SUM(TONGTIEN) AS TONGTIEN FROM VE WHERE (NGAYDATVE BETWEEN ? AND ?) GROUP BY NGAYDATVE";
            PreparedStatement pres = conn.prepareStatement(SQL);
            ngaybd = String.valueOf(new java.sql.Date(jDateChooser1.getDate().getTime()));
            ngaykt = String.valueOf(new java.sql.Date(jDateChooser2.getDate().getTime()));
            pres.setDate(1, new java.sql.Date(jDateChooser1.getDate().getTime()));
            pres.setDate(2, new java.sql.Date(jDateChooser2.getDate().getTime()));
            ResultSet rs = pres.executeQuery();
            int stt = 0;
            while(rs.next()){
                stt++;
                dtmVe.addRow(new Object[] {stt,rs.getDate("NGAYDATVE"),rs.getInt("SLVE"),rs.getInt("TONGTIEN")});
                dataset.addValue(rs.getDouble("SLVE"), "Số lượng", rs.getDate("NGAYDATVE"));
            }
            long Tong = 0;
            for(int i = 0; i < dtmVe.getRowCount(); i++){
                Tong = Tong + (int)dtmVe.getValueAt(i, 3);
            }
            txtTongDT.setText(String.valueOf(Tong));
            
            JFreeChart chart = ChartFactory.createBarChart("THỐNG KÊ SỐ VÉ BÁN ĐƯỢC TỪ NGÀY  " + ngaybd +" ĐẾN NGÀY "+ ngaykt, "Ngày", "Số lượng", dataset);
            ChartPanel chartPanel = new ChartPanel(chart);
            chartPanel.setPreferredSize(new Dimension(pn_thongke_Ve.getWidth(), pn_thongke_Ve.getHeight() - 20));
            pn_thongke_Ve.removeAll();
            pn_thongke_Ve.setLayout(new CardLayout());
            pn_thongke_Ve.add(chartPanel);
            pn_thongke_Ve.revalidate();
            pn_thongke_Ve.repaint();
        } catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_loc_VeActionPerformed

