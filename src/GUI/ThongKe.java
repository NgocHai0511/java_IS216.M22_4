            pres.setDate(2, new java.sql.Date(jDateChooser4.getDate().getTime()));
            pres.setString(3, '%'+listmaG.get(cbgaDi.getSelectedIndex())+'%');
            pres.setString(4, '%'+listmaG.get(cbgaDen.getSelectedIndex())+'%');
